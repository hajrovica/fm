<h3>POJMOVI U OBLASTI RASHODA</h3>
<br>
<div>
  <h4>1) TEKUĆI RASHODI</h4>
   <b>Operativni troškovi nastali pružanjem usluga od strane opcina kao sto su troškovi radne snage, materijala i energije, te potpore pruzene drugim budzetskim jedinicama, pravnim ili fizickim licima.</b>
<br><br>
  <b>Plaće i naknade troškova zaposlenih</b><br>
  Ukljucuju trosak bruto plaća i naknada troškova zaposlenih i ostalih skupštinskih zastupnika
<br><br>
  <b>Doprinosi poslodavca i ostali doprinosi</b><br>
  Vrsta operativnog troska koja pada na teret poslodavcu za sve zaposlenike ‘na papiru’.
<br><br>
  <b>Izdaci za materijal, sitni inventar i usluge</b><br>
  Ukljucuju putne troškove, izdatke za energiju i komunalne usluge, nabavku materijala, unajmljivanje imovine i opreme, tekuće održavanje, izdatke osiguranja bankarskih usluga, usluga platnog prometa, itd.
<br><br>
  <b>Tekući  transferi</b><br>
  Troskovi potpora namijenjeni za tekuce projekte drugih nivoa vlasti, pojedinaca, neprofitnih  organizacija te subvencije javnim i privatnim preduzećima, poduzetnicima te finansijskim institucijama.
<br><br>
  <b>Kapitalni transferi</b><br>
  Troskovi potpora namijenjeni za dugorocne, kapitalne projekte drugih nivoa vlasti, pojedinaca, neprofitnih  organizacija te subvencije javnim i privatnim preduzećima, poduzetnicima te finansijskim institucijama.
<br><br>
  <b> Izdaci za kamate</b><br>
  Troskovi kamata za primljene domace i inostrane pozajmice te ostale naknade vezane za pozajmljivanje.

</div>
<hr class="sl">
<br><br><br>
<div>
  <h4>2) KAPITALNI RASHODI</h4>
  <b>Rashodi nastali radi investicija u kapitalne projekte dugorocnog znacaja kao što su izdaci za rekonstrukciju, kupovinu stalnih sredstava i investiciono odrzavanje.</b>
<br><br>
  <b>Izdaci za nefinansijsku imovinu</b><br>
  Ukljucuju rashode za kapitalna ulaganja kao sto su nabavke šuma, zemljišta i višegodišnjih zasada, nabavke građevina, opreme te rekonstrukcija i investiciono održavanje.
<br><br>
  <b>Budzetska rezerva</b><br>
  Sluzi za finansiranje rashoda nastalih uslijed vanrednih okolnosti, kao što su poplava, zemljotres, požar, ekološka katastrofa i druge elementarne nepogode.
</div>

