<h3>POJMOVI U OBLASTI PRIHODA</h3>
<br>
<div>
  <h4>1) POREZNI PRIHODI</h4>
  <b>Prihodi od izravnih i neizravnih poreza koje je porezni obveznik dužan plaćati budžetskoj jedinici u skladu sa poreznim zakonima.</b>
<br><br>
  <b>Porezi na dohodak i dobit</b><br>
  Prihodi kojima se oporezuje dohodak fizičkih i pravnih lica te ‘porez na dohodak preduzeća’. Obuhvata: porez na dobit od privredne i profesionalne djelatnosti, porez na osnovu autorskih prava, itd.
<br><br>
  <b>Porez na lična primanja i prihode od samostalne djelatnosti</b><br>
   Uplaćuje se prema prebivalištu poreznog obveznika. Obuhvata sve novčane i nenovčane prihode i naknade koje obveznik ostvari.
<br><br>
  <b>Porez na imovinu</b><br>
  Obuhvata: poreze na posjedovanje imovine fizičkih i pravnih lica te porez na naslijeđe i darove.
<br><br>
  <b>Domaći porezi na dobra i usluge</b><br>
   Prihodi kojima se oporezuju osnovni proizvodi poljoprivrede, ribarstva, proizvoda koji sluze za prehranu, te posebni proizvodi i usluge kao sto su igre na srecu.
<br><br>
  <b>Prihodi od neizravnih poreza</b><br>
   Prihodi koji se ubiru indirektnim oporezivanjem dobara i usluga preko uvoznika, proizvodaca ili prodavaca, kao sto su porez na promet, porez na dodanu vrijednost, akcize, carine.
<br><br>
  <b>Ostali porezni prihodi</b><br>
  Obuhvataju: nespomenute poreze na potrošnju u ugostiteljstvu, poreze na firmu, poseban porez na plaću za zaštitu od prirodnih i drugih nesreća, itd.
</div>
<hr class="sl">

<br><br><br>
<div>
  <h4>2) NEPOREZNI PRIHODI</h4>
  <b>Prihodi od taksa institucija, sudskih taksa, novčanih kazni, dividendi po osnovu udjela u preduzećima, te naknade za korištenje voda, šuma i koncesije.</b>
<br><br>
  <b>Prihodi od poduzetničkih aktivnosti i imovine</b><br>
  Prihodi od nefinansijskih javnih preduzeća i finansijskih javnih institucija. Ukljucuju kamate, dividende i naknade primljene od pozajmica i učešća u kapitalu, prihode od pozitivnih kursnih razlika i privatizacije.
<br><br>
  <b>Naknade i pristojbe od pružanja javnih usluga</b><br>
  Uključuju administrativne, sudske, komunalne takse te naknade za zakup javnih površina i reklama postavljenih na javnim površinama.
<br><br>
  <b>Novčane kazne</b><br>
  Uključuju prihode od prethodno pismeno formulisanih prekršaja (prometni prekršaji) kojima je dodjeljena adekvatna novčana naknada.
<br>v
  <b>Ostali neporezni prihodi</b><br>
  Ukljucuju izostavljene prihode neporezne prirode kao sto su naknade za komisiju za izdavanje upotrebne dozvole, urbanističke saglasnosti, učešće na tenderima.
</div>

<hr class="sl">

<br><br><br>
  <div>
    <h4>3) TRANSFERI I DONACIJE</h4>
    <b>Sredstva koja dospijevaju na racun opcine sa racuna ostalih budzetskih jedinica, inostranih vlada i organizacije, ili kao ‘poklon’ od strane fizickih i pravnih lica.</b>
<br><br>
    <b>Primljeni tekući transferi od inostranih vlada i međunarodnih organizacija</b><br>
    Potpore pruzene općini za razvoj raznih projekata od strane inostranih vlada i organizacija.
<br><br>
    <b>Primljeni tekući transferi od ostalih razina vlasti</b><br>
    Potpore od ostalih budžetskih jedinica kao sto su Federacija i kantoni za različite namjene: eko fond, fond za prirodne i druge nesreće.
<br><br>
    <b>Donacije od fizičkih ili pravnih lica</b><br>
    Bespovratni prihodi, ‘pokloni’ koje općina primi od strane donatora s kojim je zaključila pisani ugovor o osiguravanju sredstava.
  </div>

<hr class="sl">
<br><br><br>
<div>
  <h4>4) KAPITALNI PRIMICI</h4>
  <b>Potpore pružene općini od strane ostalih domaćih budžetskih jedinica, inostranih vlada i organizacija te sredstava skupljenih prodajom nefinansijske i finansijske imovine.</b>
<br><br>
  <b>Kapitalni primici od prodaje stalnih sredstava</b><br>
  Primici od prodaje dugoročnih, stalnih sredstava kao sto su zemljišta, stambeni objekti te građevinska oprema.
<br><br>
  <b>Primljeni kapitalni transferi od inostranih vlada i međunarodnih organizacija</b><br>
  Nepovratne potpore inostranih vlada i organizacija u svrhu nabavka kapitalne imovine.
<br><br>
  <b>Kapitalni transferi od ostalih razina vlasti</b><br>
  Kapitalne potpore primljene od ostalih budzetskih jedinica kao sto su Federacija i kantoni za projekte dugorocnog znacaja (stanovi za socijalno ugrozene).
<br><br>
  <b>Primici od finansijske imovine</b><br>
  Povrat od učešća u dionicama javnih, privatnih preduzeća te priliv od prodaje domaćih obveznica, primljene otplate od pozajmljivanja drugim nivoima vlasti, pojedincima, neprofitnim te inostranim organizacijama.

</div>
