<h2 style="text-align:center;">Pojmovi u oblasti rashoda</h2>
<br>
<h2>Tekući rashodi</h2>
<p><br><strong><i>Operativni troškovi nastali pružanjem usluga  od strane opcina kao sto su troškovi radne snage, materijala i energije, te  potpore pruzene drugim budzetskim jedinicama, pravnim ili fizickim licima.</i>  </strong></p>
<div style="margin-left:21px;">
 <br> <p><strong>Plaće i naknade troškova zaposlenih</strong><br>
  Ukljucuju  trosak bruto plaća i naknada troškova zaposlenih i ostalih skupštinskih  zastupnika</p>
 <br> <p><strong>Doprinosi poslodavca i ostali  doprinosi</strong> <br>
    Vrsta  operativnog troska koja pada na teret poslodavcu za sve zaposlenike &lsquo;na  papiru&rsquo;.<strong></strong></p>
 <br> <p><strong>Izdaci za materijal, sitni inventar i  usluge </strong><br>
    Ukljucuju  putne troškove, izdatke za energiju i komunalne usluge, nabavku materijala,  unajmljivanje imovine i opreme, tekuće održavanje, izdatke osiguranja  bankarskih usluga, usluga platnog prometa, itd.</p>
<br>  <p><strong>Tekući  transferi</strong> <br>
    Troskovi  potpora namijenjeni za tekuce projekte drugih nivoa vlasti, pojedinaca,  neprofitnih  organizacija te subvencije  javnim i privatnim preduzećima, poduzetnicima te finansijskim institucijama.      </p>
 <br> <p><strong>Kapitalni transferi </strong><br>
    Troskovi  potpora namijenjeni za dugorocne, kapitalne projekte drugih nivoa vlasti,  pojedinaca, neprofitnih  organizacija te  subvencije javnim i privatnim preduzećima, poduzetnicima te finansijskim  institucijama.</p>
 <br> <p><strong>Izdaci za kamate</strong> <br>
    Troskovi  kamata za primljene domace i inostrane pozajmice te ostale naknade vezane za  pozajmljivanje.                                                                                                                                                                                                            <br>
</div>
<br>
<h3>Kapitalni rashodi</h3></p>
  <br><strong><i>Rashodi nastali radi investicija u  kapitalne projekte dugorocnog znacaja kao što su izdaci za rekonstrukciju,  kupovinu stalnih sredstava i investiciono odrzavanje.</i></strong>
<div style="margin-left:21px;"><br>
  <p><strong>Izdaci za nefinansijsku imovinu</strong> <br>
  Ukljucuju  rashode za kapitalna ulaganja kao sto su nabavke šuma, zemljišta i  višegodišnjih zasada, nabavke građevina, opreme te rekonstrukcija i  investiciono održavanje.</p>
 <br> <p><strong>Budzetska rezerva</strong> <br>
    Sluzi za finansiranje rashoda nastalih uslijed vanrednih okolnosti, kao što su poplava, zemljotres, požar, ekološka katastrofa  i druge elementarne nepogode.</p>
</div><br>
<h3>Otplata dugova</h3>
<p><br>
  <strong><i>Jedinice  lokalne samouprave zbog nedostatka sredstava te potencijalno velikog disbalansa  izmedu prihod i rashoda posuduju novac sto za posljedicu ima otplacivanje kredita.</i></strong></p>
<div style="margin-left:21px;">
<br>
  <p><strong>Izdaci za finansijsku imovinu</strong></p>
  Ukljucuju  izdatke pozajmljivanja drugim nivoima vlasti, pojedincima, neprofitnim  organizacijama, privatnim preduzećima, javnim preduzećima, inostranstvu te  izdatke za kupovinu dionica javnih i privatnih preduzeća.</div>

