 <br> <h2 style="text-align:center;">Pojmovi u oblasti prihoda</h2>

<br>
<h2>Porezni prihodi</h2>
<br><p><strong><i>Prihodi od izravnih i neizravnih  poreza koje je porezni obveznik dužan plaćati budžetskoj jedinici u skladu sa  poreznim zakonima.</i></strong></p>
  <br>
<div style="margin-left:21px;">

  <b>Porezi na dohodak i dobit</b> <br>
  Prihodi kojima  se oporezuje dohodak fizičkih i pravnih lica te &lsquo;porez na dohodak preduzeća&rsquo;.  Obuhvata: porez na dobit od privredne i profesionalne djelatnosti, porez na  osnovu autorskih prava, itd.</p>
  <br>
  <p><strong>Porez na lična primanja i prihode od samostalne  djelatnosti</strong> <br>
    Uplaćuje se prema prebivalištu poreznog  obveznika. Obuhvata sve novčane i nenovčane prihode i naknade koje obveznik  ostvari.</p>
  <br>
  <p><strong>Porez na imovinu</strong> <br>
    Obuhvata: poreze  na posjedovanje imovine fizičkih i pravnih lica te porez na naslijeđe i darove. </p>
  <br>
  <p><strong>Domaći porezi na dobra i usluge</strong><br>
    Prihodi kojima se oporezuju osnovni proizvodi  poljoprivrede, ribarstva, proizvoda koji sluze za prehranu, te posebni  proizvodi i usluge kao sto su igre na srecu.</p>
  <br>
  <p><strong>Prihodi od neizravnih poreza</strong><br>
    Prihodi koji se ubiru indirektnim  oporezivanjem dobara i usluga preko uvoznika, proizvodaca ili prodavaca, kao  sto su porez na promet, porez na dodanu vrijednost, akcize, carine.</p>
  <br>
  <p><strong>Ostali porezni prihodi</strong> <br>
    Obuhvataju: nespomenute poreze na potrošnju u  ugostiteljstvu, poreze na firmu, poseban porez na plaću za zaštitu od prirodnih  i drugih nesreća, itd. </p>
  <br><br>
</div>
<br>
<h2>Neporezni prihodi</h2>
<p><br><i>
<strong>Prihodi od taksa institucija, sudskih taksa,  novčanih kazni, dividendi po osnovu udjela u preduzećima, te naknade za  korištenje voda, šuma i koncesije.</strong></i></p>
<div style="margin-left:21px;">
  <br>
  <p><strong>Prihodi od poduzetničkih aktivnosti i imovine</strong><br>
  Prihodi od  nefinansijskih javnih preduzeća i finansijskih javnih institucija. Ukljucuju  kamate, dividende i naknade primljene od pozajmica i učešća u kapitalu, prihode  od pozitivnih kursnih razlika i privatizacije.</p>
  <br>
  <p><strong>Naknade i pristojbe od pružanja javnih usluga</strong><br>
    Uključuju  administrativne, sudske, komunalne takse te naknade za zakup javnih površina i  reklama postavljenih na javnim površinama.</p>
  <br>
  <p><strong>Novčane kazne</strong> <br>
    Uključuju  prihode od prethodno pismeno formulisanih prekršaja (prometni prekršaji) kojima  je dodjeljena adekvatna novčana naknada.</p>
  <br>
  <p><strong>Ostali neporezni prihodi</strong><br>
    Ukljucuju  izostavljene prihode neporezne prirode kao sto su naknade za komisiju za  izdavanje upotrebne dozvole, urbanističke saglasnosti, učešće na tenderima. </p>
  </div>
<br>
<p><h2>Transferi i donacije</h2><br>
    <strong><i>Sredstva koja dospijevaju na racun opcine sa  racuna ostalih budzetskih jedinica, inostranih vlada i organizacije, ili kao  &lsquo;poklon&rsquo; od strane fizickih i pravnih lica.</i></strong></p>
  <div style="margin-left:21px;">
<br>
  <p>Primljeni tekući  transferi od inostranih vlada i međunarodnih organizacija<br>
    Potpore pruzene  općini za razvoj raznih projekata od strane inostranih vlada i organizacija. </p>
  <br>
  <p><strong>Primljeni tekući transferi od ostalih razina  vlasti</strong> <br>
    Potpore od  ostalih budžetskih jedinica kao sto su Federacija i kantoni za različite  namjene: eko fond, fond za prirodne i druge nesreće.</p>
  <br>
  <p><strong>Donacije od fizičkih ili pravnih lica</strong><br>
    Bespovratni  prihodi, &lsquo;pokloni&rsquo; koje općina primi od strane donatora s kojim je zaključila  pisani ugovor o osiguravanju sredstava. </p>
  </div>
<br>
<h2>Kapitalni primici</h2>
<p><br><strong><i>Potpore pružene općini od strane ostalih domaćih  budžetskih jedinica, inostranih vlada i organizacija te sredstava skupljenih  prodajom nefinansijske i finansijske imovine.</i>  </strong></p>
<div style="margin-left:21px;">
  <br>
  <p><strong>Kapitalni primici od prodaje stalnih sredstava</strong><br>
  Primici od  prodaje dugoročnih, stalnih sredstava kao sto su zemljišta, stambeni objekti te  građevinska oprema.</p>
  <br>
  <p><strong>Primljeni kapitalni transferi od inostranih vlada  i međunarodnih organizacija</strong> <br>
    Nepovratne  potpore inostranih vlada i organizacija u svrhu nabavka kapitalne imovine.</p>
  <br>
  <p><strong>Kapitalni transferi od ostalih razina vlasti</strong><br>
    Kapitalne  potpore primljene od ostalih budzetskih jedinica kao sto su Federacija i  kantoni za projekte dugorocnog znacaja (stanovi za socijalno ugrozene).</p>
  <br>
  <p><strong>Primici od finansijske imovine</strong> <br>
    Povrat od učešća  u dionicama javnih, privatnih preduzeća te priliv od prodaje domaćih obveznica,  primljene otplate od pozajmljivanja drugim nivoima vlasti, pojedincima,  neprofitnim te inostranim organizacijama.</p>
</div>
